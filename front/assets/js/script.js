/**
 * Animações ao scroll
 */
function initScrollAnimations() {
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animate-in');
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);
    
    const animateElements = document.querySelectorAll('.service-card, .testimonial-card');
    animateElements.forEach(el => observer.observe(el));
}

/**
 * Mask para telefone
 */
function initPhoneMask() {
    const phoneInputs = document.querySelectorAll('input[type="tel"], #telefone');
    
    phoneInputs.forEach(input => {
        input.addEventListener('input', function(e) {
            let value = e.target.value.replace(/\D/g, '');
            
            if (value.length > 11) value = value.slice(0, 11);
            
            if (value.length > 10) {
                value = value.replace(/^(\d\d)(\d{5})(\d{4}).*/, "($1) $2-$3");
            } else if (value.length > 6) {
                value = value.replace(/^(\d\d)(\d{4})(\d{0,4}).*/, "($1) $2-$3");
            } else if (value.length > 2) {
                value = value.replace(/^(\d\d)(\d{0,5}).*/, "($1) $2");
            } else if (value.length > 0) {
                value = value.replace(/^(\d*)/, "($1");
            }

            e.target.value = value;
        });
    });
}

/**
 * Custom Alert Modal
 */
function customAlert(message, title = 'Alerta') {
    const modal = document.getElementById('customAlert');
    const modalTitle = document.getElementById('modalTitle');
    const modalMessage = document.getElementById('modalMessage');
    const modalBtn = document.getElementById('modalBtn');
    const modalClose = document.getElementById('modalClose');

    if (!modal || !modalTitle || !modalMessage || !modalBtn || !modalClose) {
        alert(message);
        return;
    }

    modalTitle.innerText = title;
    modalMessage.innerText = message;
    modal.classList.add('active');

    function closeModal() {
        modal.classList.remove('active');
    }

    modalBtn.onclick = closeModal;
    modalClose.onclick = closeModal;

    modal.addEventListener('click', function(e) {
        if (e.target === modal) {
            closeModal();
        }
    });
}