function initHeader() {
    const header = document.getElementById('header');
    
    if (header) {
        window.addEventListener('scroll', function() {
            if (window.scrollY > 50) {
                header.classList.add('scrolled');
            } else {
                header.classList.remove('scrolled');
            }
        });
    }
}

/**
 * Popup Promocional
 */
function initPopup() {
    const popup = document.getElementById('popupOverlay');
    const closeBtn = document.getElementById('popupClose');
    const skipBtn = document.getElementById('popupSkip');
    const form = document.getElementById('popupForm');

    if (popup && !sessionStorage.getItem('popupClosed')) {
        setTimeout(() => {
            popup.classList.add('active');
        }, 5000);
    }

    function closePopup() {
        if (popup) {
            popup.classList.remove('active');
            sessionStorage.setItem('popupClosed', 'true');
        }
    }

    if (closeBtn) closeBtn.addEventListener('click', closePopup);
    if (skipBtn) skipBtn.addEventListener('click', closePopup);

    if (popup) {
        popup.addEventListener('click', function(e) {
            if (e.target === popup) {
                closePopup();
            }
        });
    }

    if (form) {
        form.addEventListener('submit', function(e) {
            e.preventDefault();
            
            const contactInput = form.querySelector('.popup-input');
            
            if (!contactInput || !contactInput.value.trim()) {
                alert(t('alertInvalidContact'));
                return;
            }
            
            const value = contactInput.value.trim();
            const phoneDigits = value.replace(/\D/g, '');
            const isValidPhone = phoneDigits.length >= 11;
            const isValidEmail = value.includes('@') && value.includes('.');
            
            if (!isValidPhone && !isValidEmail) {
                alert(t('alertInvalidFormat'));
                return;
            }
            
            const btn = form.querySelector('button[type="submit"]');
            const originalContent = btn.innerHTML;

            btn.innerHTML = `<i class="fas fa-spinner fa-spin"></i> ${t('sending')}`;
            btn.classList.add('btn-loading');
            btn.disabled = true;
            
            setTimeout(() => {
                alert(t('alertPopupSuccess'));

                form.reset();
                closePopup();
                
                btn.innerHTML = originalContent;
                btn.classList.remove('btn-loading');
                btn.disabled = false;
            }, 1500);
        });
    }
}

/**
 * Menu Mobile - Toggle
 */
function initMobileMenu() {
    const menuToggle = document.getElementById('menuToggle');
    const navMenu = document.getElementById('navMenu');
    
    if (menuToggle && navMenu) {
        menuToggle.addEventListener('click', function() {
            navMenu.classList.toggle('active');
            
            const spans = menuToggle.querySelectorAll('span');
            if (navMenu.classList.contains('active')) {
                spans[0].style.transform = 'rotate(45deg) translate(5px, 5px)';
                spans[1].style.opacity = '0';
                spans[2].style.transform = 'rotate(-45deg) translate(5px, -5px)';
            } else {
                spans[0].style.transform = 'none';
                spans[1].style.opacity = '1';
                spans[2].style.transform = 'none';
            }
        });
        
        const navLinks = navMenu.querySelectorAll('.nav-link');
        navLinks.forEach(link => {
            link.addEventListener('click', function() {
                navMenu.classList.remove('active');
                const spans = menuToggle.querySelectorAll('span');
                spans[0].style.transform = 'none';
                spans[1].style.opacity = '1';
                spans[2].style.transform = 'none';
            });
        });
    }
}

